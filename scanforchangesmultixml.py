def build_note_block(pdf_refs):
    lines = [""]
    for pdf, pages in sorted(pdf_refs.items()):
        lines.append("  * " + pdf + "  p. " + ", ".join(pages))
    return "\n".join(lines)


def note_already_added(existing, pdf_refs):
    if not existing:
        return False
    return any(pdf in existing for pdf in pdf_refs)


def run_apply(game_refs, all_xml_data, xml_output_map, review_csv):
    from lxml import etree

    if not os.path.isfile(review_csv):
        print("ERROR: Review CSV not found:")
        print("  " + review_csv)
        print("Run with MODE = 'review' first to generate it.")
        sys.exit(1)

    # Group approved matches by XML input file
    approved_by_xml = defaultdict(dict)
    skipped_blank = []

    with open(review_csv, newline="", encoding="utf-8") as fh:
        for row in csv.DictReader(fh):
            scanned  = row["SCANNED_NAME"].strip()
            title    = row["APPROVED_TITLE"].strip()
            xml_file = row.get("XML_FILE", "").strip()
            if title and xml_file and xml_file != "NO MATCH":
                approved_by_xml[xml_file][scanned] = title
            else:
                skipped_blank.append(scanned)

    total_approved = sum(len(v) for v in approved_by_xml.values())
    print("")
    print("Approved matches to apply:  " + str(total_approved))
    print("Skipped (blank title):      " + str(len(skipped_blank)))

    total_applied = 0
    total_already = 0
    total_not_found = []

    for xml_input, output_xml in xml_output_map.items():
        if xml_input not in all_xml_data:
            continue

        approved = approved_by_xml.get(xml_input, {})
        if not approved:
            print("")
            print("[" + os.path.basename(xml_input) + "] No approved matches - skipping.")
            continue

        print("")
        print("[" + os.path.basename(xml_input) + "] Applying "
              + str(len(approved)) + " match(es)...")

        tree, lb_titles = all_xml_data[xml_input]
        applied    = 0
        already_done = 0
        not_in_xml = []

        for scanned, lb_title in sorted(approved.items()):
            pdf_refs = game_refs.get(scanned)
            if not pdf_refs:
                print("  [WARN] '" + scanned + "' not in games_found.txt - skipping")
                continue

            elem = lb_titles.get(lb_title)
            if elem is None:
                not_in_xml.append(lb_title)
                print("  [WARN] '" + lb_title + "' not found in XML - skipping")
                continue

            notes_el = elem.find("Notes")
            existing = (notes_el.text or "") if notes_el is not None else ""

            if note_already_added(existing, pdf_refs):
                print("  [SKIP]    '" + lb_title + "' - references already in Notes")
                already_done += 1
                continue

            note_block = build_note_block(pdf_refs)
            new_notes  = existing.rstrip() + note_block

            if notes_el is None:
                notes_el = etree.SubElement(elem, "Notes")
            notes_el.text = new_notes

            refs_summary = ", ".join(sorted(pdf_refs))
            print("  [UPDATED] '" + lb_title + "'  <-  " + refs_summary)
            applied += 1

        print("  Updated: " + str(applied) + "  |  Already done: " + str(already_done))

        if not_in_xml:
            print("  Not found in XML: " + str(len(not_in_xml)))
            for t in not_in_xml:
                print("    - " + t)
            total_not_found.extend(not_in_xml)

        if applied == 0:
            print("  No changes to write for this file.")
        else:
            tree.write(output_xml, pretty_print=True, xml_declaration=True, encoding="utf-8")
            print("  Saved to: " + output_xml)

        total_applied += applied
        total_already += already_done

    print("")
    print("=" * 60)
    print("Total updated:      " + str(total_applied))
    print("Total already done: " + str(total_already))
    print("Total skipped:      " + str(len(skipped_blank)))
    if total_not_found:
        print("Total not in XML:   " + str(len(total_not_found)))
    print("")
    print("WARNING: Original XML files were NOT modified.")
    print("Review the _updated files before replacing your originals.")


