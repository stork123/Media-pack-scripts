def run_review(game_refs, all_xml_data, review_csv, threshold):
    """
    Matches scanned games against titles from ALL loaded XML files combined.

    CSV columns:
        SCANNED_NAME   - game name found in PDFs (do not edit)
        APPROVED_TITLE - LaunchBox title to update (EDIT THIS COLUMN)
        XML_FILE       - which XML file this match came from (do not edit)
        MATCH_SCORE    - confidence 0-100, "NO MATCH" if nothing found
        ALTERNATIVE_1  - second best match (may be from a different XML)
        ALTERNATIVE_2  - third best match
        PDF_REFS       - page references from the scan (do not edit)

    How to edit:
        - APPROVED_TITLE is pre-filled with the best fuzzy guess
        - Change APPROVED_TITLE if it is wrong
        - Clear APPROVED_TITLE to skip that game entirely
        - XML_FILE tells you which file will be updated for that row
    """
    install_if_missing("thefuzz", "thefuzz")
    install_if_missing("python-Levenshtein", "Levenshtein")

    # Build combined title -> xml_path map across all XML files
    # If the same title exists in multiple XMLs, first one wins
    combined_titles = {}
    for xml_path, (tree, lb_titles) in all_xml_data.items():
        for title in lb_titles:
            if title not in combined_titles:
                combined_titles[title] = xml_path

    print("")
    print("Matching " + str(len(game_refs)) + " scanned games against "
          + str(len(combined_titles)) + " titles across "
          + str(len(all_xml_data)) + " XML file(s)  (threshold=" + str(threshold) + ")")
    print("")

    rows = []
    no_match_count = 0

    for scanned in sorted(game_refs.keys()):
        pdf_refs = game_refs[scanned]
        refs_str = " | ".join(
            pdf + ": p." + ",".join(pages)
            for pdf, pages in sorted(pdf_refs.items())
        )

        matches = find_top_matches(scanned, combined_titles.keys(), threshold, n=3)

        if not matches:
            no_match_count += 1
            rows.append({
                "SCANNED_NAME":   scanned,
                "APPROVED_TITLE": "",
                "XML_FILE":       "NO MATCH",
                "MATCH_SCORE":    "NO MATCH",
                "ALTERNATIVE_1":  "",
                "ALTERNATIVE_2":  "",
                "PDF_REFS":       refs_str,
            })
            print("  [NO MATCH]     " + scanned)
        else:
            best_title, best_score = matches[0]
            best_xml = combined_titles[best_title]

            def alt_str(m):
                return m[0] + " [" + os.path.basename(combined_titles[m[0]]) + "]"

            alt1 = alt_str(matches[1]) if len(matches) > 1 else ""
            alt2 = alt_str(matches[2]) if len(matches) > 2 else ""
            score_str = "(exact)" if best_score == 100 else "(" + str(best_score) + "%)"
            xml_short = os.path.basename(best_xml)
            print("  [MATCH " + score_str + "]  '" + scanned + "'  ->  '" + best_title + "'  [" + xml_short + "]")
            rows.append({
                "SCANNED_NAME":   scanned,
                "APPROVED_TITLE": best_title,
                "XML_FILE":       best_xml,
                "MATCH_SCORE":    best_score,
                "ALTERNATIVE_1":  alt1,
                "ALTERNATIVE_2":  alt2,
                "PDF_REFS":       refs_str,
            })

    fieldnames = [
        "SCANNED_NAME", "APPROVED_TITLE", "XML_FILE", "MATCH_SCORE",
        "ALTERNATIVE_1", "ALTERNATIVE_2", "PDF_REFS"
    ]
    with open(review_csv, "w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    matched_count = len(game_refs) - no_match_count
    print("")
    print("=" * 60)
    print("Total scanned:         " + str(len(game_refs)))
    print("Matched (pre-filled):  " + str(matched_count))
    print("No match found:        " + str(no_match_count))
    print("")
    print("Review CSV saved to:")
    print("  " + review_csv)
    print("")
    print("Next steps:")
    print("  1. Open matches_review.csv in Excel or a text editor")
    print("  2. Check APPROVED_TITLE - edit if wrong, clear to skip")
    print("  3. XML_FILE shows which XML will be updated for each row")
    print("  4. Save the CSV")
    print("  5. Change MODE = 'apply' and run again")
    print("")


