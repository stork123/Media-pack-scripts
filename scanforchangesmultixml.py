# PARSE games_found.txt
# -----------------------------------------------------------------------------

def parse_games_found(path):
    """
    Reads games_found.txt and returns:
        { scanned_game_name: { pdf_filename: [page_strings] } }
    """
    games = defaultdict(lambda: defaultdict(list))

    with open(path, encoding="utf-8") as fh:
        content = fh.read()

    master_match = re.search(r"MASTER INDEX.*?\n=+\n(.*)", content, re.DOTALL)

    if master_match:
        body = master_match.group(1)
        current_game = None
        for line in body.splitlines():
            if not line.strip():
                continue
            if not line[0].isspace():
                current_game = line.strip()
            elif current_game:
                for sep in ("--", "\u2014"):
                    if sep in line:
                        parts = line.split(sep, 1)
                        pdf = parts[0].strip()
                        pages = re.sub(r"^p\.\s*", "", parts[1].strip())
                        if pdf:
                            games[current_game][pdf].append(pages)
                        break
    else:
        print("Note: MASTER INDEX section not found - using per-file section.")
        current_pdf = None
        for line in content.splitlines():
            stripped = line.strip()
            if re.match(r"^\S.*\.pdf$", stripped, re.IGNORECASE):
                current_pdf = stripped
            elif current_pdf and "p." in line:
                for sep in ("--", "\u2014"):
                    if sep in line:
                        parts = line.split(sep, 1)
                        game = parts[0].strip()
                        pages = re.sub(r"^p\.\s*", "", parts[1].strip())
                        if game:
                            games[game][current_pdf].append(pages)
                        break

    return {k: dict(v) for k, v in games.items()}


