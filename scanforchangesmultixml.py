def discover_xml_files(folder):
    """
    Find all .xml files in folder, excluding any already ending in _updated.xml.
    Returns list of (input_path, output_path) tuples.
    """
    if not os.path.isdir(folder):
        print("ERROR: XML_FOLDER not found:")
        print("  " + folder)
        sys.exit(1)

    pairs = []
    for filename in sorted(os.listdir(folder)):
        if not filename.lower().endswith(".xml"):
            continue
        # Skip files we previously generated
        if filename.lower().endswith("_updated.xml"):
            continue
        input_path  = os.path.join(folder, filename)
        base        = os.path.splitext(filename)[0]
        output_path = os.path.join(folder, base + "_updated.xml")
        pairs.append((input_path, output_path))

    return pairs


