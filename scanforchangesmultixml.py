# LOAD LAUNCHBOX XML
# -----------------------------------------------------------------------------

def load_xml(path):
    from lxml import etree
    parser = etree.XMLParser(remove_blank_text=False)
    tree = etree.parse(path, parser)
    root = tree.getroot()
    elems = root.findall(".//Game")
    if not elems:
        elems = root.findall("Game")
    lb_titles = {}
    for elem in elems:
        t = elem.find("Title")
        if t is not None and t.text:
            lb_titles[t.text] = elem
    return tree, lb_titles


# -----------------------------------------------------------------------------
# FUZZY MATCHING
# -----------------------------------------------------------------------------

def normalize(name):
    n = name.lower()
    n = re.sub(r"\s*[\(\[].*?[\)\]]", "", n)
    n = re.sub(r"[^a-z0-9\s]", "", n)
    n = re.sub(r"\b(the|a|an)\b", "", n)
    return re.sub(r"\s+", " ", n).strip()


def find_top_matches(query, candidates, threshold, n=3):
    from thefuzz import fuzz, process
    q_norm = normalize(query)
    norm_map = {normalize(c): c for c in candidates}
    results = process.extract(
        q_norm,
        list(norm_map.keys()),
        scorer=fuzz.token_sort_ratio,
        limit=n
    )
    return [(norm_map[r[0]], r[1]) for r in results if r[1] >= threshold]


